public class conditionals {
    public static void main(String[] args) {
        int num=0;
        // for loop

        for (int i = 0; i < 100; i++) {
            System.out.println(i);

        }

        //while loop
        while (num<5){
            System.out.println("hello world");
            num+=1;
        }




    }


}
